package cl.uach.info090;
/**
 * Para cada tipo de datos primitivo (int, byte, short, float, double, etc)
 * existe una Wrapper class (o clase envoltorio). Para casi todos los efectos, 
 * java maneja un objeto Integer de la misma forma que una variable int, aunque 
 * conceptualmente son muy distintos. Por ejemplo un Integer puede ser null.
 *  
 * @author Julio D. Guerra
 *
 */

public class WrapperClassesExample {

	public static void main(String[] args) {
		// Wrapper Clases
		
		int i1 = 10;
		Integer i2 = null;
		
		double d1 = 0.5;
		Double d2 = 0.5;
		
		//Boolean
		//Character
		//Byte
		//Float
		//Long
		//Short
	}

}
